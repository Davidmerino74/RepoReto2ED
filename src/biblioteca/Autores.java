package biblioteca;

public class Autores {
    private int id_autor;
    private String nombre;
    private String apellidos;
    
    ////////////////////CONSTRUCTORES/////////////////////
	/** CONSTUCTOR COMPLETO
	 * @param id_autor
	 * @param nombre
	 * @param apellidos
	 */
	public Autores(int id_autor, String nombre, String apellidos) {
		super();
		this.id_autor = id_autor;
		this.nombre = nombre;
		this.apellidos = apellidos;
	}

    
}

